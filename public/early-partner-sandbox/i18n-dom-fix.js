  function fixDocument() {
    const lang = (document.documentElement.getAttribute("lang") || "").toLowerCase();
    const map = MAPS[lang];
    if (!map) return;

    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    const textNodes = [];
    let node;
    while ((node = walker.nextNode())) {
      if (!node.nodeValue) continue;
      if (!normalize(node.nodeValue)) continue;
      textNodes.push(node);
    }

    textNodes.forEach((n) => {
      const original = normalize(n.nodeValue);
      if (map[original]) {
        n.nodeValue = n.nodeValue.replace(original, map[original]);
      }
    });

    document.querySelectorAll("title, meta[name='description']").forEach(() => {});
  }

